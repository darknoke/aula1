public class ex1012 {
    public static void main(String[] args) {
        System.out.println("FOR");
        for(int i=1;i<101;i++)
            System.out.println(i +" ");
        System.out.println("\nWHILE");
        int k=1;
        while(k<101){
            System.out.println(k +" ");
            k++;
        }
        System.out.println("\nDO WHILE");
        k=1;
        do{
            System.out.println(k +" ");
            k++;
        }while(k<101);
        
        
    }
}
